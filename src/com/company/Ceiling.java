public class Ceiling {
    private int colour;
    private int paintedColor;

    public Ceiling(int colour, int paintedColor) {
        this.colour = colour;
        this.paintedColor = paintedColor;
    }

    public int getColour() {
        return colour;
    }

    public int getPaintedColor() {
        return paintedColor;
    }
}
